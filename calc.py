import math
# Constants 
# ------------------
PI = math.pi
E = math.e
# ------------------

def sin(x):
    return math.sin(x)

def cos(x):
    return math.cos(x)

def tan(x):
    return math.tan(x)

def asin(x):
    return math.asin(x)

def acos(x):
    return math.acos(x)

def atan(x):
    return math.atan(x)

def exp(x):
    return math.exp(x)

def gcd(x, y):
    return math.gcd(x, y)

def fabs(x):
    return math.fabs(x)

def ceil(x):
    return math.ceil(x)

def floor(x):
    return math.floor(x)

def degrees(x):
    return math.degrees(x)

def radians(x):
    return math.radians(x)

def log(*args):
    return math.log(*args)

def lcm(x, y):
    return (x * y) / gcd(x, y)
